#!/bin/bash
load(){
    echo -e "\n"
    bar=" >>>>>>>>>>>>>>>>>>>>> [100%] COMPLETED    "
    barlength=${#bar}
    i=0
    while((i<100)); do
        n=$((i*barlength / 100))
        printf "\e[00;32m\r[%-${barlength}s]\e[00m" "${bar:0:n}"
        ((i += RANDOM%5+2))
        sleep 0.2
    done
}
echo
load
echo
